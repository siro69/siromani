						<div class="main-content text-left px-0 px-md-5">
							<p class="text-uppercase">You need to be firm and confident while presenting yourself in an interview. You need to create an impression among the employers and recruitment team to get an edge over the other candidates. You can have a look at the following on how to make yourself the most potential one.</p>
							<p>
								<span class="highlight-part"><i class="fab fa-slack-hash"></i> Presenting yourself in person:</span><br>
								Conquering the job interview Once you’ve acquired an invitation to interview, you’ve already captivated the employer on paper. To present yourself nice and well in the interview and obviously make a good impression on the employer.
							</p>
							<p>
								<span class="highlight-part"><i class="fab fa-slack-hash"></i> Plan on how you meet the employer’s requirements </span><br>
								You perhaps spent a lot of time working with the job details when you combine your cover letter and resume. Now that you’ve got an interview, you’ll want to review it. Give a close attention to the qualifications, skills and job duties. These are essentially the lists of the employer’s requirement.
							</p>
							<p>
								<span class="highlight-part"><i class="fab fa-slack-hash"></i> Three ways to get ready for the interview:</span><br>
								These are how you can make yourself the best among the rest:
							</p>
							<p>
								<span class="second-highlight">1. Research the organization</span><br>
								Before attending for the interview you should know all of the information possible from internet, newspaper or other source which shows later on that you do keen interest on that company from the interviewer side.
							</p>
							<p>
								<span class="second-highlight">2. Figure out the common interview questions</span><br>
								For example, the narratives that illustrate your most specific and prominent skills. It’s fine that you bring in your resume and portfolio with you just to catch the eye of the employer or the recruiter.
							</p>
							<p class="questions">
								<span class="second-highlight">Here are some of the example of question that may asked you in interview</span><br>
								<span><i class="far fa-question-circle"></i> Tell me about yourself:</span><br>
								<span><i class="far fa-question-circle"></i> Don’t assume the recruitment team to remember the details and of your resume. They won’t summarize your accomplishments,</span><br>
								<span><i class="far fa-question-circle"></i> your work, and skills.</span><br>
								<span><i class="far fa-question-circle"></i> Why do you want to work for our company / organization?</span><br>
								<span><i class="far fa-question-circle"></i> Why do you want this role / position?</span><br>
								<span><i class="far fa-question-circle"></i> What are your strengths and weaknesses?</span><br>
								<span><i class="far fa-question-circle"></i> Can you tell us about your previous company and your role there</span><br>
							</p>
							<p>
								<span class="second-highlight">3. Prepare your own questions</span><br>
								Just like a handshake or a warm greeting, a one-sided interview is not that fun! The hiring team wants to look and hear your questions as well. They want to see whether you’re actively engaged in learning more about the organization, culture and position.
							</p>
